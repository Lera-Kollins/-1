def find_index(list_of_product, product):
    index_first_occurrence = None
    for index, value in enumerate(list_of_product):
        if product == value:
            index_first_occurrence = index
            break
    return index_first_occurrence


items_list = ['яблоко', 'банан', 'апельсин', 'груша', 'киви', 'банан']

for find_item in ['банан', 'груша', 'персик']:
    index_item = find_index(items_list, find_item)  # TODO Вызовите функцию, что получить индекс товара
    if index_item is not None:
        print(f"Первое вхождение товара '{find_item}' имеет индекс {index_item}.")
    else:
        print(f"Товар '{find_item}' не найден в списке.")
